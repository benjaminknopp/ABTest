md"""
# A/B testing

In diesem Notebook untersuche ich den Einfluss der Dauer eines A/B Tests auf den Gewinn innerhalb eines Jahres. Dabei nutze ich simulierte Datensätze und klassische Nullhypothesentests für die Entscheidung über Annahme des Features.

Das Notebook ist interaktiv (auch der Text): Es ist erwünscht, mit den Schiebern, aber auch den Variablen zu spielen.
"""

# ╔═╡ f33d2a8a-eed2-4ace-966d-a4760f544015
md"""
!!! info "TLDR"
	In dem sehr simplifizierten Szenario unter der Annahme, dass das Feature ein kleines bisschen mehr Gewinn bringt sollte man eine höhere Testpower anstreben als die (in der Psychologie) üblichen ``0.8``. Eine höhere Power lohnt sich, da sich der Gewinnunterschied klein ist und das Experiment daher recht günstig. Über den Lauf des Jahres zahlt es sich dann aus, dass nicht fälschlicherweise von einem nicht signifikanten Ergebnis ausgegangen wird.

	Die Simulationen sind sehr nützlich, um eine Intuition für die statistischen Effekte zu gewinnen. Dabei sollten die getroffenen Annahmen immer kritisch überprüft werden.
"""

# ╔═╡ 3e9ead91-4da0-43ed-a1f8-b97763b48a40
begin
	new_users_week = 40000
	pf0 = 0.1
	pf1 = 0.104
	price = 7
end

# ╔═╡ 8276445e-685e-4c15-8c49-00d5655706dd
md"""
## Beispiel


Als simples Beispiel nehmen wir an, dass ein neues Feature bei neuen User*innen ausprobiert werden soll. Das Feature könnte die Wahrscheinlichkeit beinflussen, dass innerhalb einer bestimmten Zeit (z.B. eine Woche), ein Premium Account gekauft wird.

Nehmen wir an es gibt $(new_users_week) neue User*innen pro Woche. Innerhalb eines Jahres wollen wir die Kosten des Experimentes wieder reinholen.
"""

# ╔═╡ fe47fa01-8b84-432b-8549-e47d8f1ab7a8
md"""
Schon eine Steigerung der Wahrscheinlichkeit von $(pf0) auf $(pf1) führt zu einer merkbaren Gewinnsteigerung von $(@sprintf("%.2f", new_users_week * (pf1 - pf0) * price * 52))€ im Jahr.
"""

# ╔═╡ 5e30cd11-684a-4e41-ab2d-9085be553ec0
begin
	n_worlds = 100
	g0 = rand(Bernoulli(pf0), (new_users_week*52, n_worlds))
	g1 = rand(Bernoulli(pf1), (new_users_week*52, n_worlds))
	g0train = rand(Bernoulli(pf0), (new_users_week*52, n_worlds))
	g1train = rand(Bernoulli(pf1), (new_users_week*52, n_worlds))
	to_table(g) = [sum(g), sum(1 .- g)]
end;

# ╔═╡ 7aa69af6-43ce-4067-9f62-80d31cf8617b
md"""
### Datensimulation

Wir simulieren $(n_worlds) Welten um verschiedene A/B Test Methoden zu vergleichen.
"""

# ╔═╡ 7f9fb7b4-12a3-4e44-885f-84ea0c885f95
md"""
Hier sind die Gewinnsteigerungen der 100 Simulationen. Über das gesamte Jahr lohnt es sich immer das feature einzuführen. Jetzt können wir untersuchen, ob Entscheidungen auf Grundlage der A/B Tests zu dem selben Ergebnis kämen.
"""

# ╔═╡ 1107a975-ba48-409e-b8fc-2c5560382a34
(mean(g1, dims=1) .- mean(g0, dims=1)) .* (price * 52 * new_users_week)

# ╔═╡ d67ea319-a184-4eff-b5fd-65a700fda73b
@bind n_weeks_adhoc Slider(1:52, show_value=true)

# ╔═╡ 7c5c4713-8168-43a3-bb05-7b2f7567dff6
n_adhoc = n_weeks_adhoc * new_users_week

# ╔═╡ de172022-7331-4a48-9582-4388f8d23a5a
md"""
### Bestimmung der Experimentdauer

Mit dem Slider unten kann die Experimentdauer in Wochen eingestellt werden. Diese Daten werden verwendet um den Jahresgewinn abzuschätzen. Erst bei ab 2 Wochen ist in allen Welten das Ergebnis der Schätzung eine Gewinnsteigerung für das Jahr.

Für $(sum(mean(g1[1:n_adhoc, :], dims=1) .< mean(g0[1:n_adhoc, :], dims=1))) Welten wird ein geringerer Gewinn geschätzt bei einer Experimentdauer von $(n_weeks_adhoc) Wochen.
"""

# ╔═╡ 51e40223-7cc6-43ef-af6e-fb1c9cdac144
(mean(g1[1:n_adhoc, :], dims=1) .- mean(g0[1:n_adhoc, :], dims=1)) .* (price * 52 * new_users_week)

# ╔═╡ b7857300-8efe-4e2b-b5ca-2b19956dba68
@bind exp_weeks Slider(1:52, default = 4, show_value = true)

# ╔═╡ 9858c07a-1de7-4373-a02b-c614fda7a0ad
n_testsubjects = exp_weeks * new_users_week

# ╔═╡ af996200-895a-484e-af9f-1d0a5d61cae5
begin
	g0idx = sample(1:n_testsubjects, n_testsubjects ÷ 2, replace=false)
	g1idx = collect(setdiff(Set(1:n_testsubjects), Set(g0idx)))
	size(g0idx) == size(g1idx)
end;

# ╔═╡ 33a6a5d1-3a1c-4004-9046-cf3d43fa64a9
@handcalc d = (pf1 - pf0) / √(0.9*0.1)

# ╔═╡ 32ef0fb0-07e8-4eb4-97fa-b13af4c1b1f6
md"""
Für $(exp_weeks) Wochen Dauer ist die Power:
"""

# ╔═╡ 8e57be9a-5727-490a-8649-23d37487d13b
power = mean(
	[ 0.05 > pvalue(
		ChisqTest(
			to_table(g1train[g1idx, i]), 
			to_table(g0train[g0idx, i]))) for i in 1:n_worlds])

# ╔═╡ 6ae34f19-f76b-4753-bd46-c79afb6ec8b1
md"""

### Klassischer Ansatz mit frequentistischen statistischen Null-hypothesis Test

Wir nutzen ein separates Trainingsset von $(n_worlds) Simulationen, um die Power ``\beta`` des Tests abzuschätzen. Unser gewünschter Effekt den wir detektieren wollen ist sehr klein (Cohen's d = $(@sprintf("%.5f", d))).

!!! info "Finde die Power"
	Nutze den Slider um die Wochenzahl des Experimentes einzustellen, und beobachte die geschätzte Power des Tests! Die Schätzungen schwanken dabei, weil 100 simulierte Datensätze nicht besonders viel sind.

Ungefähr $(n_testsubjects) User*innen reichen für eine Power von $power. Dafür brauchen wir mit unseren Annahmen $(exp_weeks) Wochen!
"""

# ╔═╡ 4ce6ecd5-dabf-4fcd-bb89-4e7ab0127a2b
function f(row1, row0)
	N = n_testsubjects
	if pvalue(ChisqTest(to_table(row1[g1idx]),
						to_table(row0[g0idx]))) < 0.05
		return (sum(row1[g1idx]) + sum(row0[g0idx]) + sum(row1[N:end]) - sum(row0)) .* price
	else
		return (sum(row1[g1idx]) + sum(row0[g0idx]) + sum(row0[N:end]) - sum(row0)) .* price
	end
end

# ╔═╡ dc7a8668-91a9-4ee4-a65d-c4eb439afde5
md"""
Die Erwartete Gewinnsteigerung (gemittelt über alle Welten) für ein $exp_weeks - wöchiges Experiment ist dann $(mean([f(g1[:, i], g0[:, i]) for i in 1:n_worlds])) €.
"""

